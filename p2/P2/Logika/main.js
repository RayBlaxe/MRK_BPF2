// operator and
let x=30;
let y=20;
if(x<5 && y<5){
    console.log("x dan y kurang dari 5");
} else{
    console.log("x dan y lebih dari 5");
}
// operator or
let nilaiUts=50;
let nilaiuas=78;
if (nilaiUts>70 || nilaiuas>80){
    console.log("Kamu Lulus");
} else {
    console.log("Kamu tidak Lulus");
}
// operator not
let jumlahKriminal=2;
if (!jumlahKriminal>1){
    console.log("Lulus SKCK")
}else{
    console.log("Tidak lulus SKCK")
}
